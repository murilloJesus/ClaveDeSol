								@section('title')
									 Projeto de musicalização (Educador)
								<br />A Música como Aliada no Desenvolvimento
								@endsection
								
								<header>
								<p align="center">Visão geral:</p>
								</header>
								<p>
									O Instituto visa a experiência dos alunos com a música de forma a englobar aspectos ainda não adquiridos ou até mesmo aprimorar os desenvolvidos.
								</p>
								<p>
									Todo aluno inserido neste instituto apresenta déficit em alguma área, podendo ser de âmbito motor, visual, afetivo, social, comportamental ou  cognitivo, que após um momento de observação e avaliação em aula é possível compreender muitas vezes o que gostam e o que mesmo aparentando simples, traz danos prejudiciais, como: barulho excessivo (hiperacusia e acusia – hipersensibilidade auditiva), aglomeração de pessoas no mesmo ambiente, sons confusos e misturados, desordem do local.
								</p>
								<p>
									Os aspectos citados acima devem ser avaliado para que de forma profissional seja planejado para o recebimento do aluno de forma mais individual possível.
								</p>
								<h3>Planejamento da aula:</h3>
								<p>
									Os alunos deverão ser recebidos no espaço da aula com:
								</p>
								<ul>
									<li>
										Materiais já organizados;
									</li>
									<li>
										 Mobiliário preparado se necessário, podendo trocar cadeiras por colchonetes ou tatames;
									</li>
									<li>
										Na aula é possível utiliza materiais, como: bola, tecido, bambolês, papéis em suas diversidades, quadro branco de azulejo, chocalho confeccionados pelos alunos com diversas sementes ou grãos, utilização do próprio corpo, objetos do cotidiano para representação concreta e etc.
									</li>
								</ul>
								<p>
									Vale lembrarmos que, o abstrato para a maior parte dos alunos é de grande dificuldade para compreensão.
								</p>
								<h3>Conteúdo da aula:</h3>
								<p>
									Todo trabalho musical desenvolvido é focado nos P.T.s, assim como o trabalho pedagógico.
								</p>
								<p>
									O Instituto apresenta abaixo o <b>Plano de Trabalho</b> que deve ser seguido e permeiam os conteúdos necessários a serem propostos em cada turma, cabendo ao regente de tais aulas buscar os  recursos dentro da Música o sucesso para o aprendizado do aluno.
								</p>
								<h3>O Musicista terá 3 direcionamentos a seguir:</h3>
								<ul>
									<li>
										Utilizar como norte os P.T.s que determina o nível de necessidade da sala;
									</li>
									<li>
										Receber um Projeto Pedagógico que dará a direção dos assuntos que deve explorar;
									</li>
									<li>
										Ter em mente que, tudo o que for proposto deve estar de acordo com: a faixa etária do aluno, parear a necessidade do mesmo, respeitar a tolerância do aluno (acusia e hiperacusia, hipersensibilidade ao toque, choros excessivos e outros).
									</li>
								</ul>
								<h3>Planos de trabalho:</h3>
								<p>
									Lembramos que, todo conteúdo exposto abaixo deve ser realizado através da música (sonorização, ritmo, tempo e outros), apoiando no trabalho com o indivíduo de forma a englobar os aspectos afetivos, motores, corporais, sociais, cognitivos e comportamentais, dentro de sua necessidade.
								</p>
								<h3>P.T.S.- (plano de trabalho de socialização)</h3>
									<p>
										Plano desenvolvido para alunos que apresentam baixo rendimento, alunos  totalmente comprometidos cognitivamente.
									</p>
										<p>
											Nesta aula os alunos necessitam muito de estímulo sensorial, de forma bastante lúdica e concreta, aumentando a atenção e concentração.
										</p>
									<h3>Coordenação Motora:</h3>
								<ul>
									<li>
										 Explorar objetos, próprio corpo;
									</li>
								</ul>
									<h3>Estimulação:</h3>
								<ul>
									<li>
										estimular a visão e audição;
									</li>
								</ul>
								<h3>
									Noções Básicas:
								</h3>
								<ul>
									<li>Tempo (quanto tempo preciso estar aqui...);</li>
									<li>Lugar (para onde irei depois daqui...);</li>
									<li>Espaço (o que devo fazer nesse local...);</li>
								</ul>
								 <h3>Socialização:</h3>
								 <ul>
								  	<li>
								  	   permitir que o aluno perceba, aceite e se interaja com o próximo, desenvolver jogos cooperativos; confecção de bandinha com sucatas;
								  	</li>
								 </ul>
								 <h3>P.T.1- (plano de trabalho de pré-alfabetização – 1º ano)</h3>
								 <p>
								 	Plano desenvolvido para alunos que possuem um rendimento médio, já apresentam uma compreensão razoável, porém um comportamento desordenado (não aceita regras, limites, realiza todas as suas vontades se descontrola com facilidade, apresenta agressividade), e não apresenta nenhum conteúdo pedagógico.
								 </p>
								 <p>
								 	Nesta turma os alunos necessitam adquirir a reorganização pessoal, aceitar regras, limites, tempo e adquiri conhecimentos gerais nos âmbitos, sensoriais, afetivos e cognitivos
								 </p>
								 <h3>Conteúdos:</h3>
								 <ul>
								 	<li>
								 		Sensoriais- estimular todos os sentidos;
								 	</li>
								 	<li>
								 		Afetivos- estreitar vínculos na aceitação do grupo e do próximo
								 	</li>
								 	<li>
								 		Cognitivos- oferecer atividades que estimulem a atenção/concentração e a realização de comandos.
								 	</li>
								 </ul>

								 <h3> Coordenação Motora:</h3>
								 <ul>
								 	<li>
								 		 Explorar objetos, próprio corpo;
								 	</li>
								 </ul>
								 <h3> Estimulação: </h3>
								 <ul>
								 	<li>
								 		 Na marcha, na fala (cantar com os alunos), no brincar, na aceitação e percepção do próximo;
								 	</li>
								 </ul>
								 <h3> Noções Básicas:</h3>
								 <ul>
								 	<li>
								 		Noção de tempo – dia (calendário);
								 	</li>
								 	<li>
								 		Lugar- onde estou e para onde vou (organização pessoal);
								 	</li>
								 	<li>
								 		Espaço- limites e regras (como devo me comportar, até onde tenho espaço);
								 	</li>
								 </ul>
								 <h3> Socialização:</h3>
								 <ul>
								 	<li>
								 		 Respeito com o outro, aumentar o tempo de tolerância, como devo me comportar neste momento, aceitar o outro e etc.
								 	</li>
								 </ul>
								 <h3>P.T.2-(plano de trabalho para sala - 2º ano- ensino fundamental)</h3>
								 <p>
								 	Plano foi desenvolvido para alunos que apresentam uma compreensão, porém de baixa aceitação a determinações. Apresentam grande dificuldade na assimilação e aquisição de conteúdo. Alunos que possuem dificuldade de atenção/concentração e precisam ser estimulados em tempo integral até mesmo em suas atividades da vida diária.
								 </p>
								 <p>
								 	Plano desenvolvido para alunos que necessitam ser estimulados cognitivamente e reorganizado para que possam vir adquirir conhecimento e estrutura para vida social com qualidade.
								 </p>
								 <h3>Apoio educacional:</h3>
								 <ul>
								 	<li>
								 		Roda da conversa;
								 	</li>
								 	<li>
								 		Emocionômetro;
								 	</li>
								 	<li>
								 		Identificação de letras através da música;
								 	</li>
								 	<li>
								 		Associação de letra/ som
								 	</li>
								 	<li>
								 		Associação de figuras a palavras;
								 	</li>
								 	<li>
								 		Reconhecimento de números;
								 	</li>
								 	<li>
								 		Noção de quantidade;
								 	</li>
								 	<li>
								 		Calendário (dias da semana);
								 	</li>
								 	<li>
								 		Autonomia;
								 	</li>
								 	<li>
								 		Show de talentos;
								 	</li>
								 	<li>
								 		Teatros;
								 	</li>
								 	<li>
								 		Danças.
								 	</li>
								 </ul>
								 <h3>Socialização:</h3>
								 <ul>
								 	<li>
								 		Incluir o aluno no grupo de forma bastante descontraída, favorecer a interação dos alunos, apresentar o respeito e a forma de tratar o outro.</h3>

								 	</li>
								 </ul>
								 <h3>P.T.3-(plano de trabalho para sala - 3º ano-ensino fundamental)</h3>
								 <p>
								 	Plano de Trabalho foi desenvolvido para alunos que já possuem as noções da alfabetização, porém se encontram na fase da construção do pensamento, compreendem e aceitam as determinações, porém a dificuldade de aprendizagem é bastante acentuada e lenta. Necessário desenvolver a capacidade crítica e as habilidade cognitiva.
								 </p>
								 <h3>Apoio Educacional:</h3>
								 <ul>
								 	<li>
								 		Reconhecimento das letras;
								 	</li>
								 	<li>
								 		Construção de frases verbalizadas (produção de música);
								 	</li>
								 	<li>
								 		Continuação de um conto ou de uma música (imaginação);
								 	</li>
								 	<li>
								 		Expressão do pensamento e senso crítico;
								 	</li>
								 	<li>
								 		Jogos com música, utilizando o corpo e objetos (bola, pano, bambolê);
								 	</li>
								 	<li>
								 		Qual é a música?
								 	</li>
								 	<li>
								 		Situações sociais;
								 	</li>
								 	<li>
								 		Saúde;
								 	</li>
								 	<li>
								 		Meio ambiente;
								 	</li>
								 	<li>
								 		Direitos/deveres;
								 	</li>
								 	<li>
								 		Contação de histórias através da música;
								 	</li>
								 	<li>
								 		Danças;
								 	</li>
								 	<li>
								 		Jogos cooperativos e brincadeiras coletivas.
								 	</li>
								 </ul>
								 <h3>P.T.4(plano de trabalho para sala– 4º ano-ensino fundamental)</h3>
								 <p>
								 	Nível Silábico Alfabético-Plano para alunos que possuem uma boa compreensão, apresentam um comportamento mais focado e organizado e necessitando de auxílio pedagógico e reorganização social e do pensamento. Auxiliar na construção do pensamento e na expressão do mesmo com definição e propriedade. Estimular a socialização, autonomia, autoestima e aceitação de diversas situações rotineiras que modificam a vida em geral.
								 </p>
								 <h3>Apoio Educacional:</h3>
								 <ul>
								 	<li>
								 		Reconhecimento das letras;
								 	</li>
								 	<li>
								 		Exposição de uma figura, para criar uma música;
								 	</li>
								 	<li>
								 		Construção de frases verbalizadas (produção de música);
								 	</li>
								 	<li>
								 		Continuação de um conto ou de uma música (imaginação);
								 	</li>
								 	<li>
								 		Quantidade e sistema monetário através da música;
								 	</li>
								 	<li>
								 		Expressão do pensamento e senso crítico;
								 	</li>
								 	<li>
								 		Jogos com música, utilizando o corpo e objetos (bola, pano, bambolê);
								 	</li>
								 	<li>
								 		Qual é a música?
								 	</li>
								 	<li>
								 		Situações sociais;
								 	</li>
								 	<li>
								 		Saúde;
								 	</li>
								 	<li>
								 		Meio ambiente;
								 	</li>
								 	<li>
								 		Direitos/deveres;
								 	</li>
								 	<li>
								 		Contação de histórias através da música;
								 	</li>
								 	<li>
								 		Danças;
								 	</li>
								 </ul>
								 <h3>  Socialização:</h3>
								 <ul>
