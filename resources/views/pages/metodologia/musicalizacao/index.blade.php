								 <h3>P.T.5- (plano de trabalho para sala - 5º ano - ensino fundamental)</h3>
								 <p>
								 	 Plano desenvolvido para alunos que apresentam uma boa compreensão, possuem conhecimentos nos conteúdos pedagógicos e estão na Fase- Alfabética, apresentam dependência nas atividades que visam autonomia/insegurança em atividades coletivas/independência A.V.D., situações que precisam ser organizadas para concluir o processo de aprendizagem.
								 </p>
								 <h3>P.T.5.A- (plano de trabalho para sala de aceleração- inclusão)- (5º ano)
								 </h3>
								 <p>
								 	 Plano desenvolvido para alunos que já possuem apresentam um bom senso crítico, adquiriu uma boa organização pessoal e independência. Essa sala tem como objetivo principal estruturar e aprimorar a aprendizagem do aluno para ser incluído na rede regular.
								 </p>
								 <h3>Apoio Educacional:</h3>
								 <ul>
								 	<li>
								 		Composição de música;
								 	</li>
								 	<li>
								 		Criar uma música através de uma figura ou objeto;
								 	</li>
								 	<li>
								 		Estimulação da criatividade;
								 	</li>
								 	<li>
								 		 Jogos corporais;
								 	</li>
								 	<li>
								 		Expressão do pensamento crítico;
								 	</li>
								 	<li>
								 		Domínio nas noções de espaço/tempo/tamanho;
								 	</li>
								 	<li>
								 		Saúde;
								 	</li>
								 	<li>
								 		Bullying;
								 	</li>
								 	<li>
								 		Meio ambiente;
								 	</li>
								 	<li>
								 		Direitos/ deveres;
								 	</li>
								 	<li>
								 		Autonomia;
								 	</li>
								 	<li>
								 		Fatos sociais através da música;
								 	</li>
								 	<li>
								 		Gincanas musicais.
								 	</li>
								 </ul>
								 <h3>Socialização: </h3>
								 <p>
								 	Expressão do pensamento crítico, roda de conversa para iniciação, integrar os alunos como grupo, trabalhar as divergência a medida que surgirem, trabalho com o respeito e espaço do outro ao se expor.
								 </p>
								 <p>
								 	O Instituto deseja que as aulas se apresentem de forma dinâmica, divertida, acolhedora e prazerosa.
								 </p>
								 <h3> Psicomotricidade e a música:</h3>
								 <p>
								 	A psicomotricidade nos traz a possibilidade de trabalharmos o indivíduo através do seu corpo e movimento, com a relação do mundo interior e externo.
								 </p>
								 <p>
								 	Nossos alunos necessitam vivenciar situações em que a música traga a percepção corporal, através de dinâmicas e de certa forma algo significativo para a construção de uma compreensão do que o cerca e de si.
								 </p>
								 <p>
								 	É através desse trabalho que conseguiremos desenvolver de maneira mais efetiva as questões da atenção, concentração, assimilação, memorização, bem como trazer as aulas jogos corporais e músicas de envolvimento corporal.
								 </p>
								 <p>
								 	A grande intenção deste trabalho e associar o lúdico aos conteúdos pedagógicos, podendo trabalhar inúmeros itens que envolvam os sentidos de percepção sensorial, com atividades de olhos vendados, que tragam cores, que envolvam objetos, que busquem fazer com que o movimento corporal e a interação entre o grupo seja efetiva.
								 </p>
