									<h2 align="center">Plano de Trabalho de Socialização (PTS)</h2>
								</header>

								<p>
									Objetivo esse plano de trabalho, deve ser oferecido aos alunos que possuem baixo rendimento, e quenecessitam de muito auxílio em seus afazeres, devido apresentarem grande déficit de cognitivo)
								</p>

								<p>
									<b>Obs.:déficits cognitivos:</b> são alterações na maneira como o indivíduo processa uma informação. Está relacionado a inteligência. Envolve principalmente funções mentais como a memória, linguagem, raciocínio lógico e abstrato, atenção, percepção, capacidades executivas e capacidade visio espacial.
								</p>

								<p>
									De uma maneira mais simples, podemos dizer que cognição é a forma como o cérebro percebe, aprende, recorda e pensa sobre toda informação captada.
								</p>

								<h3>
									Conteúdo:
								</h3>

								<p>
									Oferecer ao aluno situações que o ajudem a entender o mundo que o cerca com atividades sensoriais. O <b>sistema sensorial</b> é constituído pelos <a href="https://pt.wikipedia.org/wiki/Receptor_sensorial">receptores sensoriais</a>, pelos <a href="https://pt.wikipedia.org/wiki/Neur%C3%B3nio#Receptores_ou_sensitivos_.28aferentes.29">neurônios aferentes</a> e pelas partes do <a href="https://pt.wikipedia.org/wiki/C%C3%A9rebro">cérebro</a> envolvidas no processamento da informação: a <a href="https://pt.wikipedia.org/wiki/Vis%C3%A3o">visão</a>, a <a href="https://pt.wikipedia.org/wiki/Audi%C3%A7%C3%A3o">audição</a>, o <a href="https://pt.wikipedia.org/wiki/Tato">tato</a>, o <a href="https://pt.wikipedia.org/wiki/Paladar"> paladar</a> e o <a href="https://pt.wikipedia.org/wiki/Olfato">olfato</a>, há já muitos cientistas que afirmam que o ser humano tem muitos outros sentidos, mas os mais conhecidos se focam nos citados acima.
								</p>

