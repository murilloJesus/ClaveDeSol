										Estimular a visão (coordenação visiomotora)
									</li>

								</ul>

								<h3>
									Usar o quadro de rotina para ajudar na reorganização emocional nas questões de:
								</h3>

								<ul>

									<li>
										Tempo (quanto tempo preciso estar aqui...);
									</li>

									<li>
										Lugar (para onde irei depois daqui...);
									</li>

									<li>
										Espaço (o que devo fazer nesse local...);
									</li>

								</ul>

								<h3>
									Usar do método Teacch, para auxiliar nas atividades utilizando do apoio visual (muitos não têm a compreensão das palavras, mas o visual o ajuda a compreender o que se diz);
								</h3>

								<ul>

									<li>
										Permitir que o aluno perceba, aceite e se interaja com o próximo;
									</li>

									<li>
										Desenvolver jogos cooperativos;
									</li>

									<li>
										Usar de músicas;
									</li>

