										Confecção de bandinha com sucatas; ( o som auxilia na atenção/concentração);
									</li>

									<li>
										Sala de TV (filmes curtos e musicais);
									</li>

									<li>
										Sala de informática,usar do computador como um aliado as atividades de classe (reforço);

									</li>

								</ul>


