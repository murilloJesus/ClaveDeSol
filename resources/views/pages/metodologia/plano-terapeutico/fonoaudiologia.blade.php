								<h3>
									Justificativa 
								</h3>

								<p>
									O presente documento tem como objetivo apresentar as contribuições, a amplitude do fazer fonoaudiólogo no contexto educacional e esclarecer prováveis dúvidas sobre essa área de competência. Mais especificamente, apoiadas nos princípios éticos que regem a profissão, nas políticas públicas de Saúde e Educação. 
								</p>
								<p>
									A Fonoaudiologia é a ciência que tem como objeto de estudo a comunicação humana em todas as suas dimensões e ciclos de vida. Abrange os aspectos referentes à linguagem oral, escrita e sinalizada, fluência, articulação da fala, voz, às estruturas/ funções orofaciais (respiração, mastigação e deglutição), funções auditiva e vestibular (equilíbrio) e aos sistemas de comunicação alternativos, aumentativos ou suplementares, entre outros. 
								</p>
								<p>
									A interface da Fonoaudiologia com a Educação pode se dar por meio de ações clínicas e de prevenção e promoção da Saúde pela demandas relativas aos problemas da comunicação humana e à aprendizagem.
								</p>
								<p>
									A atuação da fonoaudiologia neste instituto de educação especial pressupõe na intervenção preventiva, orientativa e interativa com atividades de promoção da saúde e qualidade de vida nos diversos contextos institucional. 
								</p>
								<h3>
									  Objetivos gerais
								</h3>
								<p>
									Em parceria com a Educação, a Fonoaudiologia agregará neste instituto conhecimentos de sua competência, contribuindo para o aprimoramento dos processos educativos de ensino-aprendizagem colaborando com as estratégias educacionais.
								</p>
								<p>
									Objetiva atuar nos aspectos que envolvem a comunicação e sua relação com a aprendizagem, considerando a aquisição, o desenvolvimento e as possíveis dificuldades nesses processos.
								</p>
								<p>
									Junto a equipe multiprofissional desenvolver utilizar técnicas que os auxilie na prática educacional.
								</p>
								<p>
									Promover as potencialidades comunicativas, afetivo-emocionais, sociais e aprendizagem.
								</p>
								<h3>
									 Objetivos específicos 
								</h3>

								<ul>
									<li>
										Definir o perfil, as necessidades e as prioridades institucionais concernentes aos aspectos fonoaudiólogos, que possam afetar as condições de Saúde e de Educação;
									</li>
									<li>
										Planejar ações em conjunto com os profissionais envolvidos no acompanhamento dos alunos, para garantir a flexibilização, adaptação e temporalidade curricular e promover ações que favoreçam a comunicação em prol da melhoria do ambiente organizacional e das relações interpessoais;
									</li>
									<li>
										Colaborar na realização de atividades promotoras de Saúde, que potencializem a aquisição, o desenvolvimento e o aprimoramento dos aspectos relacionados à linguagem oral, escrita e sinalizada, voz, audição e às funções e estruturas orofaciais;
									</li>
									<li>
										Orientar a comunidade escolar sobre hábitos saudáveis no que se refere à linguagem oral e escrita, voz, audição e às estruturas e funções orofaciais;
									</li>
									<li>
										Realizar ações formativas, diretamente, voltadas às famílias, aos alunos e aos diversos atores sociais da comunidade escolar sobre assuntos pertinentes à Fonoaudiologia.
									</li>
									<li>
										Auxiliar na identificação e na condução das demandas relativas às dificuldades fonoaudiológicas apresentadas pela comunidade escolar;
									</li>
									<li>
										Sensibilizar a comunidade escolar sobre as interferências dos problemas de saúde relacionados ao processo educacional;
									</li>
									<li>
										Realizar contato e articula as informações dos diferentes profissionais, da rede de atenção, envolvidos no cuidado dos alunos;
									</li>
									<li>
										Incentivar e apoia a interlocução entre os profissionais de Saúde e a escola;
									</li>
									<li>
										Assegurar o estabelecimento do fluxo de contra referência para o apoio dos educadores e dos escolares;
									</li>
									<li>
										Auxiliar e realiza orientações a pais/responsáveis, familiares e cuidadores com foco nas necessidades dos alunos;
									</li>
									<li>
										Participar das reuniões pedagógicas, conforme necessidades levantadas pela equipe técnica e/ou escolar;
									</li>
									<li>
										Contribuir para o diagnóstico das situações de risco à saúde auditiva e à saúde vocal do educador, aponta possibilidades de intervenções e encaminhamentos para a equipe multidisciplinar;
									</li>
									<li>
										Realizar ações direcionadas ao aprimoramento das habilidades comunicativas (fala, voz e fluência) da equipe escolar;
									</li>
									<li>
										Contribuir para a inclusão efetiva, promovendo a acessibilidade na comunicação e auxiliando na definição dos melhores meios e técnicas de intervenção. 
									</li>
								</ul>
