									<h2 align="center"> Plano de trabalho  </h2>
									<p align="center">Terapia ocupacional educacional - 2021</p>
								</header>
								<h3> Justificativa:  </h3>
								<p>
								    A Terapia Ocupacional estuda a atividade humana e utiliza como recurso terapêutico para prevenir as dificuldades física ou psicossociais que interferem no desenvolvimento e na independência do aluno, em relação as atividades de vida diária, trabalho e lazer. Com intervenção na saúde, educação e na esfera social, reunindo tecnologias orientadas para a emancipação e autonomia de pessoas que, por razões ligadas à problemática específica, físicas, sensoriais, mentais, psicológicas e/ou sociais apresentam temporariamente ou definitivamente dificuldade da inserção e participação na vida social.
								</p>
								<p>
									No ambiente escolar, o trabalho de Terapia Ocupacional consiste em analisar o processo e desenvolvimento das habilidades adquiridas, para depois introduzir a prática, suprindo as necessidades de cada aluno.
								</p>
								<p>
									 As práticas terapêuticas estarão entrelaçadas com os processos educacionais, sendo assim, os objetivos principais da Terapia Ocupacional dentro do ambiente escolar visão trabalhar e desempenhar suas funções específicas em conjunto com os serviços educacionais, colaborando de forma técnica e eficiente para que os alunos encontrem um nível satisfatório de qualidade de vida.
								</p>
								<h3>Objetivos Geral:</h3>
								<ul>
									<li> Estruturação de rotina do aluno.
									</li>
									<li>
										Orientação para os professores em sala de aula para organização do cotidiano escolar.
									</li>
									<li>
										Orientação para os responsáveis.
									</li>
									<li>
										Com a equipe terapêutica planejar e adaptar as melhores soluções para o ambiente escolar.
									</li>
									<li>
										 Facilitar o aprendizado por meio de estratégias lúdicas, e realizar treinos das habilidades.
									</li>
									<li>
										Promover independência e autonomia dos alunos nas atividades de vida diária.
									</li>
									<li>
										Treinos de alimentação, escovação de dentes e uso de banheiro.
									</li>
									<li>
										  Confeccionar adaptações e materiais de apoio ao aluno, quando necessário, visando ampliar sua participação nas atividades
									</li>
									<li>
										Favorecer respostas e estímulos sensoriais através de atividades que envolvam uso dos sentidos.
									</li>
									<li>
										Adaptações em materiais didáticos.
									</li>
									<li>
										Adaptações em mobiliário, pertences, utensílios e ao que for adaptável dentre o ambiente escolar que traga acessibilidade ao aluno.
									</li>
									<li>
										 Intervir com alunos nas dificuldades de aprendizado ao que refere à alteração na coordenação motora.
									</li>
								</ul>
								<p>
									Tecnologia assistiva - qual treina e capacita os professores para o uso da tecnologia adaptada ou projetadas, para a melhora das funcionalidades consequentes a deficiência ou mobilidade reduzida.
								</p>
								<h3>Objetivos Específicos:</h3>
								<ul>
									<li>
										  Estimular a socialização intuindo facilitar a iniciativa e interesse do aluno.
									</li>
									<li>
										 Desenvolver regras e limites dentro do ambiente escolar, visando a rotina, promovendo a qualidade de vida e o bem estar de todos que estiverem no contexto.
									</li>
									<li>
										Intervir no desempenho das habilidades neuropsicomotoras.
									</li>
									<li>
										Estimular a independência e autonomia, através das realizações das AVBD’s e AVBP’s (atividades básicas da vida diária e prática)
									</li>
								</ul>
								<h3>Métodos e Técnicas:</h3>
								<p>
									    De acordo com o programa educacional do instituto a Terapia Ocupacional tem como função desempenhar e facilitar o aprendizado dos alunos com deficiência ao acesso à educação, pois acredita na oportunidade do indivíduo desenvolver aspectos intelectuais, social, emocional e físicos, por meio de participação no espaço educacional, melhorando e possibilitando a qualidade de vida e para isto utilizará de diversas atividades interventivas e de autocuidado, afim de restaurar <b>a capacidade dos alunos, atividades como recurso terapêutico com contexto da ocupação  humana.</b>
								</p>
								<h3>Estratégias:</h3>
								<p>
									<b>O Terapeuta Ocupacional sendo o profissional dentro do âmbito escolar que pode atuar para a</b> <a href="https://pt.wikipedia.org/wiki/Preven%C3%A7%C3%A3o">prevenção</a>, <a href="https://pt.wikipedia.org/wiki/Habilita%C3%A7%C3%A3o">habilitação</a> ou reabilitação, então as estratégias utilizadas pela terapeuta ocupacional está direcionada para contribuir  com a manutenção e aquisição de habilidades, sejam elas motoras ou cognitivas, para um melhor desempenho escolar, sendo assim oferece atividades especificas que cooperem para o ganho de autonomia  e independência.
								</p>
								<p>
									Visa o trabalho com a equipe multidisciplinar para contribuir com a sua vertente e também por necessitar de amparo dos olhares das demais ações terapêuticas, já que a terapia ocupacional inclina para o desenvolvimento humano, contribuirá dando suporte aos profissionais em relação as atividades X aluno e orientações mediadoras e interventivas aos aos pais e familiares.
								</p>
								<h3>Terapia Ocupacional no Instituto</h3>
								<p>
									O terapeuta ocupacional em contexto escolar deve utilizar da Classificação Internacional de Funcionalidade Incapacidade e Saúde (CIF) que é também uma ferramenta de política pública para melhorias na educação, por que classifica não somente as funções do corpo e suas estruturas, mas classifica primordialmente as atividades e a participação do educando em diferentes momentos como: escrita, mobilidade dentro e fora da escola, atividades manuais, cuidados pessoais fundamentais no cotidiano escolar como se alimentar, ir ao banheiro, brincar, se socializar, auxiliar no desenvolvimento  de habilidades motoras, cognitivas e sociais que desempenhem em atividades cotidianas .
								</p>
								<p>
									Conhecendo as atividade e participações do educando será possível classificar também quais são as barreiras para a execução das atividades propostas pela escola e quais são os facilitares para o processo ensino aprendizagem.
								</p>
								<p>
									O principal objetivo do terapeuta ocupacional na escola é promover a facilitação dos processos de aprendizagem por meio de atividades lúdicas e interativas e integrativas.
								</p>
								<p>
									O terapeuta ocupacional possui um importante papel na orientação da família sobre o desenvolvimento, nas habilidades sociais, atividades de ocupação de vida diária, pratica e escolar, sendo assim visa favorecer para o olhar da família no potencial para as habilidades e as quais devem ser desenvolvidas, nas insuficiências do aluno e na desconstrução dos obstáculos para potencializar o desempenho ocupacional e a inclusão.
								</p>
								<p>
									 Diante disso a terapia ocupacional junto a equipe terapêutica tem como função promover ações que vai trabalhar as dificuldades de cada aluno, para que assim possam ter uma estratégia, para possível intervenção.
								</p>
								<p>
									 A terapia ocupacional tem o papel de identificar junto ao instituto possíveis dificuldades inerentes a condição de totalidade da aprendizagem, sendo assim auxiliar com planejamentos, intervenções, orientações e resoluções dos problemas apresentado, afim de obter comportamento saudáveis neste contexto.
								</p>
								<h3>Conclusão:</h3>
								<p>
									A terapia ocupacional no ambiente escolar tem uma contribuição que perpasse a restruturação das escolas, da atenção individualizada do aluno, dando visibilidade e um conhecimento maior em suas potencialidades, assim como alcance satisfatório do meio sociocultural em que o aluno está inserido.
								</p>
								<p>
									 As intervenções da T.O contribuirá com a educação, trazendo um conceito funcional, que se trata das adaptações que tragam conteúdos pedagógicos para a realidade daqueles que possuem déficits como objetivo de assimilar e utilizar o conhecimento  de maneira funcional, de acordo com a capacidade singular do aluno em atenção, concentração, raciocínio lógico e memória, podendo ser tratado pelo profissional da terapia ocupacional através de técnicas e métodos   visando propiciar melhor qualidade de vida, a autonomia e a independência, além destes promover condições para que cada discente em sua particularidade possa vivenciar possibilidades.
								</p>
