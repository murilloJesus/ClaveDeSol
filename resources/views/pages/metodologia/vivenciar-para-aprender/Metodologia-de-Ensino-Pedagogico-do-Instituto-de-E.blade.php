								<h3>Visão Geral</h3>
								<p>
									O Instituto visa a qualidade de ensino na vivência do aprender...
								<br>
									Todo aluno com distúrbios de aprendizagem ou ainda laudado que exija uma atenção maior no seu desenvolvimento, seja ele de âmbito, motor, visual, afetivo, social, comportamental ou  cognitivo, que após uma avaliação psicopedagógica, possa ser inserido no Instituto deve Vivenciar para aprender....
								</br>
								</p>
								<h3>
									Metodologia vivenciar para aprender
								</h3>
								<p>
									A <b>Metodologia Vivenciar para Aprender,</b> surgiu de críticas e sugestões sobre o programa de Ensino já estabelecido neste Instituto, que vem passando por grandes inovações, adequações em todos os setores.
									<br>
										Essa metodologia, que renova completamente o Ensino desse Instituto, visa favorecertodos os alunos a vivência  do ensino na prática,  antes da assimilação concreta (registro), de todo trabalho pedagógico.
									</br>
								</p>
								<p>
									Tudo que se for concretizar (registrar) em papéis (cadernos, folhas de sulfites ou outros), deve ser vivenciado na prática em primeiro lugar.
								<br>
									Todo trabalho pedagógico desenvolvido(projetos/conteúdos) será acompanhado dos P.T.(s) (Planos de Trabalho)
								</br>
								</p>
								<p>
									P.T.- Concreto (vivências)- Direcionado pela Psicopedagoga após a avaliação
								<br>
									P.T. -Abstrato (registros)-Elaborado pelos Professor
								</br>
								</p>
								<ul>
									<li>
											A Coordenação Pedagógica apresenta a você professor o <b>Plano de Trabalho</b> que deve seguir, os <b>Projetos Pedagógicos,</b>que permeiam os assuntos em salas e cabe avocê buscar os  recursos dentro da Metodologia determinada o sucesso do aprendizado do aluno.
									</li>
											<h3>O professor terá 3 direcionamentos a seguir:</h3>
									</ul>
								<ul>
									<li>
										 Receber um P.T. que determina o nível de aprendizagem da sala ;
									</li>
									<li>
										 Receber um Projeto Pedagógico que me dará a direção dos assuntos que deve explorar
									</li>
									<li>
										 Ter em mente que a nova metodologia exige que tudo que eu for ensinado deve servivenciado.
									</li>
								</ul>
								<h3>
									Quanto As Elaborações Dos Planos De Trabalho:
								</h3>
								<p>
									<b>
										Etapas do desenvolvimento da escritacitado na elaboração desses Planos foi retirado do Livro de Emilia Ferreiro- Alfabetização em Processo-SP-Ed. Cortez-1980, estes devem ser parâmetros de análise das etapas que o aluno percorre.
									</b>
								</p>
								<ul>
									<li>
										<b> P.T.S.-(Plano De Trabalho De Socialização),</b> ( Plano desenvolvido para alunos que apresentam baixo rendimento e não possuem nenhum conteúdo pedagógico/, alunos  totalmente comprometidos cognitivamente);
									</li>
									<li>
										<b> P.T.1-(Plano De Trabalho De Pré-alfabetização – 1º Ano),</b>(Plano desenvolvido para alunos que possuem um rendimento médio, já apresentam uma compreensão razoável, porém um comportamento desordenado (não aceita regras, limites, realiza todas as suas vontades se descontrola com facilidade, apresenta agressividade),e não apresenta nenhum conteúdo pedagógico;
									</li>
									<li>
										<b> P.T.1-(Plano De Trabalho De Pré-alfabetização – 1º Ano),</b>(Plano desenvolvido para alunos que possuem um rendimento médio, já apresentam uma compreensão razoável, porém um comportamento desordenado (não aceita regras, limites, realiza todas as suas vontades se descontrola com facilidade, apresenta agressividade),e não apresenta nenhum conteúdo pedagógico;
									</li>
									<li>
										<b> P.T.2-(Plano De Trabalho Para Sala - 2º Ano- Ensino Fundamental),</b>Plano foi desenvolvido para alunos que apresentam uma compreensão, porémde baixa aceitação a determinações. Apresentam grande dificuldade na assimilação e aquisição de conteúdo, Nível- Pré-Silábico- As partes da escrita não correspondem às partes do nome. Fase gráfica primitiva – símbolos, misturados com letras e números. Os  alunos escrevem letras, bolinhas e números, como se soubessem escrever, sem uma preocupação com as propriedades sonoras da escrita. Nesse nível o  aluno explora tanto critérios qualitativos (varia o repertório das letras ou a posição das mesmas, sem alterar a quantidade) ou critérios quantitativos (varia a quantidade de letras de uma escrita para outra, sem preocupação com as propriedades sonoras - (leitura e escrita de pequenas palavras /realização das operações no concreto, tudo com muito auxílio e pausado).Alunos que possuem dificuldade de atenção/concentração e precisam ser estimulados em tempo integral até mesmo em suas atividades da vida diária. No raciocínio lógico necessitam de auxílio nas atividades de forma concreta.
									</li>
									<li>
										<b>P.T.3-(Plano De Trabalho Para Sala - 3º Ano-ensino Fundamental),</b> (Esse Plano de Trabalho foi desenvolvido para alunos que já possuem as noções da alfabetização- Nível-Silábico- É a descoberta de que a quantidade de letras com que vai escrever uma palavra pode ter correspondência com a quantidade de partes que se reconhece na emissão oral. “Pedaços sonoros” , essas partes são as sílabas e em geral, o  aluno faz corresponder uma grafia a cada sílaba.Porém se encontram na fase da construção do pensamento, leem pequenas palavras com auxílio visual, realizam soma e subtração simples com auxílio no concreto, compreendem e aceitam as determinações, porém a dificuldade de aprendizagem é bastante acentuada e lenta.
									</li>
									<li>
										<b>P.T.4(Plano De Trabalho Para Sala– 4º Ano-ensino Fundamental)</b>- Nível Silábico Alfabético-Plano para alunos que possuem uma boa compreensão, apresentam um comportamento mais focado e organizado e precisam de estímulos pedagógicos principalmente na questão da leitura e da escrita que se encontra em pleno desenvolvimento. Nas questões do raciocínio lógico já resolvem as operações com mais independência e coerência.   Nesse nível existe a  correspondência entre sons e grafias: a sílaba é o som produzido por uma só emissão de voz. O aluno escreve parte da palavra aplicando a hipótese silábica, de que para se escrever uma sílaba é necessário apenas uma letra. Costuma usar somente as vogais, porque combina com uma porção de palavras, mas para eles em uma palavra, não pode repetir a mesma letra duas ou mais vezes numa escrita, pois assim o resultado será algo “não legível”. Nesse nível, a criança já começa a acrescentar letras na primeira sílaba.Nessa fase, a escrita já inicia um significado, porém necessita de auxílio para formulação de frases e organização do pensamento.
									</li>
									<li>
										<b>P.T.5- (Plano De Trabalho Para Sala - 5º Ano - Ensino Fundamental)</b>
										Plano desenvolvido para alunos que apresentam uma boa compreensão, possuem conhecimentos nos conteúdos pedagógicos e estão na Fase- Alfabética,  Caracteriza-se pela correspondência entre fonemas e grafias. Geralmente os alunos já conseguem ler e expressar graficamente o que pensa ou fala. Compreende a logicidade da base alfabética da escrita. Reconhece letras, escreve o nome com letra bastão/, porém apresentam muitas dificuldades no processo geral da alfabetização, necessitando de aprimoramentos, (leitura não fluente/ erros ortográficos) dependência nas atividades que visam autonomia/insegurança em atividades coletivas/independência A.V.D., situações que precisam ser organizadas para concluir o processo de aprendizagem.
									</li>
									<li>
										<b>PT O- (Plano De Trabalho Para Oficina)</b> - (Plano desenvolvido para alunos que com ênfase na unidade do Recanto)- alunos que não apresentam perspectivas para o Ensino Regular, já estão acima da idade escolar, apresentam boa ou baixa compreensão, precisam de independência em A.V.D, A.V.P., socialização, tolerância, criatividade, aperfeiçoamento de seus afazeres do dia a dia, disciplina ,  autonomia na realização de seus desejos e aptidão na resoluções de pequenos problemas sócias.(caso Sidnei)
									</li>
								</ul>
								<h3> Objetivo Do Plano De Trabalho: </h3>
								<ul>
									<li>
										<p>O principal objetivo dos P.T.(s) está nas possibilidades que os alunos terão ao se deparar com as realidades do seu cotidiano, onde nas mesmas poderão adquirir tanto o conhecimento teórico quanto nas habilidades de se tornarem mais independentes e obterem a possibilidade de desfrutar das situações que a vida rotineira oferece, com domínio de poder realizar suas próprias tarefas.</p>

										<p> Essa metodologia oferecer ao aluno dois caminhos de aprendizagem, sendo eles:
									</li> </p>
									<li>
										1º- tendo a oportunidade de tocar o mundo que o cerca, desfrutando, adquirindo ou ainda, aperfeiçoando as possibilidades de sua independência, do seu agir em suas vontades e necessidades, do seu desenvolver como ser humano que faz parte de uma sociedade.
									</li>
									<li>
										2º- Transferir para o papel o conhecimento adquirido e registrar seus atos para se tornar um cidadão alfabetizado, crítico e capaz de desenvolver suas atividades diárias como qualquer ser humano.
									</li>

								</ul>
								<h3>
									Desenvolvimento:
								</h3>
								<ul>
									<li>
										O professor receberá da Coordenação Pedagógica seu P.T.A. (Plano De Trabalho Abstrato), esse plano é desenvolvido aonível do cognitivo da turma,(direcionado pela psicopedagoga após a avaliação individual com cada aluno),  ou parte dela (caso a turma apresente diferenças intelectuais o professor pode receber até dois P.T.A.(s), para adequar a qualidade de ensino a necessidade do aluno. Cabe ressaltar que o professor também receberá bimestralmente os projetos pedagógicos, sobre o qual são confeccionados de acordo com as opiniões que os professores devem participar ou ainda deixar sua sugestão, crítica, opinião, na urna exposta na sala dos professores. É importante que todo professor participe do corpo dos projetos. Sendo assim, o projeto bimestral deverá estar dentro das atividades que os P.T(s) determinarem, o trabalho deve caminhar paralelo.
										<p>
											   Importante lembrar... Tudo que for ensinado deve ser antes vivenciado !!!
										</p>
									</li>
								</ul>
