									Dicas
								</h3>

								<p>
									“Perfil do aluno”- enviar para casa para que possamos colher dados que ainda não conhecemos dos nossos alunos, bem como coisas que acalmam, comida que não gosta, restrições alimentares, medicação, o que realmente irrita ,entre outras informações importantes para uma excelente criação de vinculo, além de podermos utilizar como recurso avaliativo ao final do semestre podendo refazer e mostrar as evoluções.
								</p>

								<p>
									“Rotina”- montar junto com os alunos a rotina de sala, de maneira lúdica e ilustrativa.
								</p>

								<p>
									Livro portifólio “eu na minha escola”, confeccionar com os alunos de forma ilustrativa um portifólio bem resumido para que eles possam manter na bolsa para nessa fase de adaptação levar para casa e trazer todos os dias, para assim assimilar o local onde estarão todos os dias ao longo do semestre.
								</p>

