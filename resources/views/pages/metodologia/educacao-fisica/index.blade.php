    @section('title')
        Plano de aula - corpo e movimento - 2021 <br / >
    @endsection
    
    <header>
        <p align="center">Justificativa:</p>
    </header>
    <p>
        As atividades que envolvem corpo e movimento favorecer o desenvolvimento global, que introduz e integra o aluno
        na cultura corporal de movimento e de uma forma completa, transmitir conhecimento, capacitando-o para usufruir
        de todas as possibilidades que esse conjunto de atividades pode agregar. É um conjunto de atividades físicas
        planejadas e estruturadas, essas atividades propostas significam educar, aprimorar e melhorar os movimentos,
        desenvolvendo o bem estar, físico e psíquico. Ela consiste em facilitar sua independência e autonomia,
        beneficiar ao aluno a percepção do próprio corpo e suas alterações, promover a socialização entre os alunos, o
        comportamento social, domínio de si, autocontrole e respeito ao próximo, no desenvolvimento orgânico e
        funcional, permitindo ao mesmo exercer todas as potencialidades, funções mentais, coordenação motora, execuções
        de movimento, criatividade, a auto expressão, a sociabilidade, no cognitivo, psicomotor e afetivo. Além de
        desenvolver em suas praticas a ampliação e diversificação da bagagem motora, assim como propor valores humanos
        positivos.
    </p>
    <p>
        O período de duração está entre 30 a 40 minutos, com o foco coletivo e individual, respeitando a individualidade
        de cada aluno, envolvendo modificações e ajustamentos das atividades tradicionais para permitir aos alunos
        participarem com segurança de acordo com suas capacidades funcionais, com estímulos a liberdade, criatividade
        individual, valorizando aquilo que cada aluno é capaz de fazer em função de suas possibilidades e limitações
        pessoais. Sendo assim o nosso objetivo é promover o desenvolvimento integral do aluno, a socialização, a vida
        saudável, espírito de equipe, aceitação de regras e seus cumprimentos, de uma forma lúdica e prazerosa, além de
        toda e qualquer manifestação da arte através do corpo.
    </p>
    <h3> Objetivo:</h3>
    <ul>
        <li>
            Desenvolver habilidades corporais (equilíbrio, coordenação motora fina e grossa);
        </li>
        <li>
            Adquirir o conhecimento de como melhorar a qualidade do movimento, conhecendo as informações essenciais das
            habilidades motoras básicas e suas combinações, para aplicá-las na organização espacial, temporal dos
            diferentes jogos;
        </li>
        <li>
            Ampliar seu repertório motor, demonstrando capacidade executar as habilidades básicas de locomoção,
            manipulação, estabilização e suas combinações;
        </li>
        <li>
            Estimular o desenvolvimento psicomotor, resgatando o movimento lúdico de receptividade, de descoberta e
            curiosidade, além de exercitar a compreensão e a afetividade, aprendendo viver no coletivo e com as
            diferenças.
        </li>
        <li>
            Estimular e incentivar para que eles adquiram capacidades de participar de atividades corporais,
            estabelecendo relações equilibradas e construtivas com as pessoas, conhecendo e respeitando características
            físicas e de desempenho de si próprio e dos outros, sem discriminar por características pessoais, físicas,
            sexuais ou sociais.
        </li>
        <li>
            Estimular e incentivar para que eles adquiram a capacidade de realizar movimentos independentes e
            interdependentes.
        </li>
        <li>
            Estimular e incentivar para que eles adquiram a capacidade de definir sua dominância lateral.
        </li>
        <li>
            Estimular o auto controle, o domínio de si mesmo e respeito ao próximo;
        </li>
        <li>
            Estimular a criatividade;
        </li>
        <li>
            Estimular a autonomia;
        </li>
        <li>
            Promover a socialização e as relações interpessoais;
        </li>
        <li>
            Desenvolver valores, atitudes de cooperação e de solidariedade.
        </li>
        <li>
            Dar oportunidade aos alunos com maiores dificuldades, através das atividades específicas.
        </li>
    </ul>
    <h3>Estrátegias:</h3>
    <ul>
        <li>
            Jogos;
        </li>
        <li>
            Brincadeiras;
        </li>
        <li>
            Circuitos;
        </li>
        <li>
            Dinâmicas;
        </li>
        <li>
            Danças
        </li>
        <li>
            Gincanas
        </li>
        <li>
            Adaptações nas atividades desenvolvimentistas, jogos, ritmos e toda manifestação da arte.
        </li>
        <li>
            Propor atividades abertas para diversidade, tanto localidades diferentes e culturais, proporcionando novas
            vivências.
        </li>
        <li>
            Adotar práticas coletivas que valorizam as diferenças e respeitam a diversidade, observando sempre as
            capacidades e habilidades individuais, e praticando uma intervenção consciente e responsável.
        </li>
    </ul>
