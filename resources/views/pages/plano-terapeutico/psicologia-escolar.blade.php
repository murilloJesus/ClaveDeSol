								<header>
									<h2 align="center"> Plano de trabalho psicologia escolar 2021 </h2>
									<p align="center">Estruturação Possibilidades e Habilidades Sociais </p>
								</header>
								<h3>Justificativa</h3>
								<p>
									É preciso reconhecer que o processo de ensino e aprendizagem implica foco em diversas áreas do conhecimento humano, pois em meio este processo dificuldades de aprendizagem acontecem, com causas, desenvolvimento e outras múltiplas questões, fazendo assim conhecimentos necessários em outras dimensões, onde entre estas a psicologia faz parte.
								</p>
								<p>
									A aprendizagem e a escola são extremamente embasadas no fazer pedagógico, com teorias do desenvolvimento e da aprendizagem para melhor compreensão do processo, consequentemente esmere ao profissional de psicologia o que acerca de estratégias e olhares diferenciados ao atendimento do indivíduo, suas particularidades, dificuldades e do conhecimento do comportamento, afim de ser contribuinte para a estruturação do contexto educacional. A psicologia escolar sendo uma área que aborda problemáticas sobre o processo         da educação, ensino e aprendizagem foca na questão social, na individualidade, no desenvolvimento global e aspectos biológicos dos discentes e todos envolvidos na questão educacional, enfim a atuação do profissional refere ao desenvolvimento humano, as relações, a integração promovendo o desenvolvimento integral do ser e do grupo institucional.
								</p>
								<p>
									Sendo assim o tema do plano de trabalho objetiva ao trabalho de atuação ao que leva o discente e os profissionais envolvidos no âmbito escolar a condição de essência pensares, sensações, emoções, relações, interação tendo como foco <b>possibilidades</b> promovendo fator significativo de vida, desenvolvimento, saúde, qualidade de vida, habilidades e aprendizagem.
								</p>
								<h3>Objetivo Geral</h3>
								<p> A profissional objetivará:</p>
								<ul>
									<li>
										Analisar as relações e fatores do sistema de ensino e como este repercuti no processo de ensino.
									</li>
									<li>
										 Auxiliar na elaboração de procedimentos educacionais capazes de atender às necessidades individuais com visão na coletividade.
									</li>
									<li>
										Conhecer o Projeto Político Pedagógico da Escola e se necessário participar da sua atualização. 
									</li>
									<li>
										Trabalhar junto à equipe pedagógica, com a coordenação e com a equipe terapêutica a fim de juntos buscar novos posicionamentos e soluções às situações apresentadas, minimizando dificuldades do contexto de aprendizagem em sua totalidade.
									</li>
									<li>
										Criar momentos de troca acerca das teorias de aprendizagem e prática pedagógica.
									</li>
									<li>
										Orientação de pais/responsáveis. 
									</li>
								</ul>
								<h3>Objetivos Específicos</h3>
								<ul>
									<li>
										Favorecer sistemas de interações, possibilitar vivencias de relações, autoafirmação automotivação, autoconhecimentos habilidades, interesses, sentimentos, propiciando atendimento interventivo e preventivo de necessidade ao conter cotidiano do discente no âmbito escolar.
									</li>
									<li>
										Permear possibilidades de intervenção.
									</li>
									<li>
										Criar espaços para escutar as demandas dos profissionais da escola e pensar maneiras de lidar com as situações apresentadas.
									</li>
									<li>
										Atuar em observação, analise e com meta interventiva.
									</li>
									<li>
										Contribuir para o desenvolvimento psicossocial
									</li>
									<li>
										 Buscar vir minimizar os comportamentos inadequados, ter olhar e possibilidades aos déficits dos discentes e buscar ampliar as condições destes. 
									</li>
									<li>
										Acompanhamento do processo de aprendizagem dos alunos que no momento demonstre apresente maior dificuldade permanente a algum contexto psicológico.
									</li>
								</ul>
								<h3>Métodos e Técnicas</h3>
								<p>
									Junto a equipe multidisciplinar, objetivar a sociabilidade, reabilitação, a qualidade de vida e o bem estar do discente.    
								</p>
								<p>
									A psicologia escolar visa as intervenções, facilitadoras de comunicação e das relações, favorecendo o comportamento, a interação/socialização, o autoconhecimento, oportunizando desenvolvimento global, buscando possibilidades e habilidades do indivíduo e do grupo, considerando a socialização, integração e qualidade de vida como metas principais. 
								</p>
								<p>
									Considerando o método TEACCH um programa educacional e clínico com uma prática predominante através da observação dos comportamentos em relação a estímulos diferentes, contarei com o método como base e parâmetro, pois além de fundamentar-se em teoria comportamental, linguagem receptiva/ expressiva, estímulos visuais e corporais este método possibilita estruturação, facilidade de compreensão, aplicação e execução para o discente.
								</p>
								<h3> Estratégias</h3>
								<p>
									Trazer intervenções estrategicamente investigativas, colaborativas, lúdicas e interventivas, a profissional dentre destas necessitará ter menção das questões de globalidade que implicam o desenvolvimento humano e também escolar, sendo necessário por vezes buscar fatos da relação professor/aluno, professor/família, aluno/instituto, aluno/família e os âmbitos que este está inserido, para melhor compreender e atuar no processo educacional.
								</p>
								<p>
									Ouvir os docentes, coordenação pedagógica suas demandas e fazê-los repensar possibilidades de novas práticas e novos olhares sobre a questão do discente.
								</p>
								<p>
									Planejar vivencias, dinâmicas, oficinas, momentos reflexivos
								</p>
								<h3>Psicologia Escolar e o Instituto</h3>
								<p>
									O trabalho Psicológico educacional estará sendo realizado com acompanhamentos, firmado em projetos socioambientais, educacionais, artesanais, dinâmicos, realizando vivências e atividades que contribuam para melhor assimilação dos discentes, visando o desenvolvimento humano, comunitário e a qualidade de vida, seguindo técnicas psicológicas de intervenção, a fim de minimizar e sanar os comportamentos situacionais inadequados.
								</p>
								<p>
									 Os acompanhamentos estarão afixado nos projetos pedagógicos tendo diferentes fermentas de intervenção em prol de beneficiar socialmente, pedagogicamente, psicologicamente, dependendo do precursor da situação aparente no contexto escolar. Os acompanhamentos promoverão dentro do Instituto possíveis condições para que o discente esteja participativo e em desenvolvimento gradual para que venha ter possibilidades, podendo aflorar o potencial ao que relaciona as questões psicossociais, psicoeducacionais e psicoemocionais. Juntamente com a equipe terapêutica, favorecer momentos esporádicos de encontros/reuniões/palestras e eventos afim de possibilitar momentos de orientação, de reflexão, de descontração e prevenção, oferecendo temáticas específicas sobre os aspectos contextualizados com a visão da margem do ambiente escolar e do instituto.
								</p>
								<h3>Conclusão</h3>
								<p>
									 Psicologia Educacional fonte de intencionalidade de transformação, mediadora e facilitadora das relações e comunicações implicada e envolvida com o contexto psicossocial, afim de produzir novos sentidos, contribuindo para o desenvolvimento comportamental, resinificando os discentes em indivíduos integrados socialmente, e os que atribuem maior maturação e compreensão, em indivíduos reflexivos, questionadores, autônomos e conscientes. 
								</p>
								<p>
									Entendendo que a vida educacional e social são marcadas por fases e fatores, e estes evocam sentimentos e emoções, marcado por aspectos pessoais, interpessoais, familiares, pedagógicos, institucionais, políticos e ideológicos, é campo de interesse da Psicologia que deve, por sua vez, fazer-se presente durante o tempo que o aluno estiver na Instituição, tornando-se disponível para auxiliar nas demandas surgidas e definir estratégias de atuação que facilite a transição, adaptação, integração da educação; contribuindo para a melhoria das condições psíquicas do aluno, que é um ser em transformação constante, trazendo consigo potencialidades e limitações.
								</p>
								<p>
									 Sendo oferecedora de propostos positivos e significativos, construindo relações, aprendizado, crescimento e amadurecimento, o serviço psicológico educacional do instituto apropria-se ao papel de identificar possíveis dificuldades e situações e auxiliar na intervenção/orientação junto a equipe terapêutica escolar, afim de obter comportamentos saudáveis.
								</p>
