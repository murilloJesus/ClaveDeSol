									<h2 align="center"> Plano de trabalho de psicopedagogia educacional - 2021 </h2>
								</header>
								<p>
									<b>A Borboleta e a Psicopedagogia:</b> O psicopedagogo coloca-se à altura da borboleta, intervindo no processo de seu nascimento, elaborando o tempo e o espaço necessários. Sem medo e insegurança, sem precipitação. Há tempo para tudo, tempo para nascer, tempo para viver, Tempo para respirar, Tempo para morrer, viver, este é o segredo. O psicopedagogo conhece a história da borboleta, acompanha o seu desenvolvimento, percebe o seu progresso, evolução, impaciência, ansiedade que deve ser superada.
								</p>
								<p>
									Esta ação é humanizante, coletiva, socialmente e com ela conquistaremos a maturidade a consciência da realidade e a humanização da borboleta.
								</p>
								<p>Esta borboleta é o Educando. <b>(Fonte: Rede Mundial)</b></p>
								<h3>Justificativa</h3>
								<p>
									 Acreditando que o indivíduo esteja em constante aprendizagem pelas possibilidades que está diariamente exposto, menciono que a psicopedagogia venha possibilitar a reintegração, reeducação ou prevenção, na conotação de olhares nas singularidades e especificidades dos indivíduos pelo contexto geral permeado de possíveis dificuldades no que emite o processo de aprendizagem.
								</p>
								<p>
									A psicopedagogia está e deve estar presente no amparo da questão global desta, exigindo o pensar critico profissional, oferecendo uma visão sensitiva e ética sobre o desenvolvimento humano e o ensino.
								</p>
								<h3>Objetivos Gerais</h3>
								<p>
									  Buscar entender, intervir e prevenir na totalidade do processo educacional, o qual mediam os indivíduos integrantes do instituto, para que venham expelir e absorver com inclusão, participação, assimilação, compreensão e construção integral para o conhecimento.
								</p>
								<p>
									Ser mediadora de atuação preventiva tendo ação reflexiva aos projetos pedagógico-educacionais e os processos didático-metodológicos.
								</p>
								<h3>Objetivos Específicos</h3>
								<p>
									Atuar nas questões preventiva, decifrar, dar suporte e intervir na origem de causas dificultosas apresentadas por discentes, docentes, profissionais e o próprio processo educacional integrado no instituto.
								</p>
								<p>
									Atentar-se nas dificuldades em questões físicas, emocionais, integrativas, mentais e outras, buscando "ferramentas" de ação social em prol a aprendizagem, autonomia, autoestima, moral, capacidades cognitivas proporcionando envolvimento e interação.
								</p>
								<p>
									Ter olhar profissional para identificar situações de controversos, de dificuldades pelo contexto da gama da educação especial, buscando estratégias, alternativas e possibilidades.
								</p>
								<p>
									Planejamento, execução e orientação de pesquisas psicopedagógicas.
								</p>
								<h3>Métodos/Técnicas/Estratégias</h3>
								<p>
									  Utilização de métodos, técnicas e estratégias psicopedagógicas, as quais apurem e tragam possibilidade e ofereçam soluções alternativas às dificuldades ajudando auxiliar a supera as mesmas.
								</p>
								<p>
									  Instrumentos psicopedagógicos que tenham por finalidade a pesquisa, a prevenção, identificação, a análise e a intervenção nos problemas do processo de aprendizagem.
								</p>
								<h3>Psicopedagogia e o Instituto  </h3>
								<p>
									  Inserida no âmbito escolar a profissional de psicopedagogia estará atuante estrategicamente agindo preventivamente e interventivamente com orientações e se necessário com atuação ativa à todos que fazem parte do instituto com a finalidade de identificar sinais significativos relacionado a demanda de aprendizagem.
								</p>
								<p>
									 Embasada na conduta, plano e atribuições de funcionamento do instituto a profissional abrangerá olhares para comportamentos, nas quais influenciam diretamente no desempenho das questões educacionais, individuais e coletivas, visando perceber e orientar aos processos educativos para minimizar situações adversas na aprendizagem da instituição, perpetuando modificação educativas para tratar os casos, podendo atuar(em casos extremos) diretamente e de modo mais individualizado com os sujeitos com problemas de aprendizagem.
								</p>
								<h3>Psicopedagogia junto a Equipe Terapêutica</h3>
								<p>
									Sendo um profissional atuante e parte da equipe multidisciplinar, pelas abordagens psicológicas, pedagógicas e principalmente necessitar positivamente de outras contribuições terapêuticas que envolve o desenvolvimento e atuar humano, sua atuação será partilhada e junto a equipe do instituto, buscará por trocas profissionais e intervenções, afim de sanar as necessidades apuradas, também para estar contribuindo com os demais profissionais terapêuticos.  
								</p>
								<h3>Conclusão</h3>
								<p>
									Consequentemente a Psicopedagogia tendo a aprendizagem humana como alvo de inquirição, o qual se ampara para sua intervenção e pesquisa em variadas vertentes como menciona (Bossa 1944), ou seja a psicopedagogia refere-se a uma área de conhecimento e de atuação profissional voltada para a <b>temática da aprendizagem do indivíduo que aprende.</b>
								</p>
								<p>
									Sendo assim, com acreditando, atuando e tendo ética, alternativas de comunicativas, sabendo ouvir, buscando equilíbrio emocional, empatia e comprometimento, nas questões dos conhecimento psicopedagógico o profissional de psicopedagogia leva a crer que o ato de pensar e de aprender deve ser valorizado e oportunizado entre as potencialidades, insuficiências, dificuldades e habilidades, ampliando a compreensão sobre as particularidades e necessidades de aprendizagem viabilizando recursos para atender a estas. 
								</p>
								<p>
									<b>Desta forma, o fazer pedagógico se transforma, podendo se tornar uma ferramenta poderosa (WEISS, 1997).</b>
								</p>

